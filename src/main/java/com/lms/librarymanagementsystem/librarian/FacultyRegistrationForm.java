    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    int selectedIndex = jTable1.getSelectedRow();

    if (selectedIndex == -1) {
        JOptionPane.showMessageDialog(this, "Please select a faculty to update");
        return;
    }

    int userId = Integer.parseInt(model.getValueAt(selectedIndex, 0).toString());

    try {
        conn.setAutoCommit(false); // START TRANSACTION

        // 1️⃣ UPDATE USERS TABLE
        pst = conn.prepareStatement(
            "UPDATE users SET full_name=?, username=?, password=?, department=? WHERE user_id=?"
        );

        pst.setString(1, txtname.getText());
        pst.setString(2, txtusername.getText());
        pst.setString(3, new String(txtpass.getPassword()));
        pst.setString(4, txtdept.getSelectedItem().toString());
        pst.setInt(5, userId);

        pst.executeUpdate();

        // 2️⃣ UPDATE FACULTY TABLE
        pst = conn.prepareStatement(
            "UPDATE faculty SET phone=?, fines=?, issued_books=? WHERE user_id=?"
        );

        pst.setString(1, txtphone.getText());
        pst.setBigDecimal(2, new java.math.BigDecimal(txtfines.getText()));
        pst.setInt(3, Integer.parseInt(txtissuebook.getText()));
        pst.setInt(4, userId);

        pst.executeUpdate();

        conn.commit(); // END TRANSACTION

        JOptionPane.showMessageDialog(this, "Faculty Updated Successfully");
        tableLoad();
        clearFields();

    } catch (Exception ex) {
        try { conn.rollback(); } catch (Exception ignored) {}
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error Updating Faculty");
    }

    }//GEN-LAST:event_jButton3ActionPerformed

