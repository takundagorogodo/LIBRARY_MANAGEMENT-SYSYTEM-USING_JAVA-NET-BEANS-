    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        

        if (txtname.getText().isEmpty()
            || txtusername.getText().isEmpty()
            || txtpass.getPassword().length == 0) {

            JOptionPane.showMessageDialog(this, "Please fill all required fields");
            return;
        }

        try {
            conn.setAutoCommit(false); // START TRANSACTION

            // 1️⃣ INSERT INTO USERS
            pst = conn.prepareStatement(
                "INSERT INTO users (full_name, username, password, role, department) VALUES (?, ?, ?, 'FACULTY', ?)",
                PreparedStatement.RETURN_GENERATED_KEYS
            );

            pst.setString(1, txtname.getText());
            pst.setString(2, txtusername.getText());
            pst.setString(3, new String(txtpass.getPassword()));
            pst.setString(4, txtdept.getSelectedItem().toString());

            pst.executeUpdate();

            // 2️⃣ GET GENERATED USER_ID
            ResultSet rs = pst.getGeneratedKeys();
            if (!rs.next()) {
                conn.rollback();
                JOptionPane.showMessageDialog(this, "Failed to create user");
                return;
            }

            int userId = rs.getInt(1);
            txtemp_id.setText(String.valueOf(userId));

            // 3️⃣ INSERT INTO FACULTY
            pst = conn.prepareStatement(
                "INSERT INTO faculty (user_id, phone, fines, issued_books) VALUES (?, ?, ?, ?)"
            );

            pst.setInt(1, userId);
            pst.setString(2, txtphone.getText());
            pst.setBigDecimal(3, new java.math.BigDecimal(txtfines.getText()));
            pst.setInt(4, Integer.parseInt(txtissuebook.getText()));

            pst.executeUpdate();

            conn.commit(); // END TRANSACTION

            JOptionPane.showMessageDialog(this, "Faculty Registered Successfully");
            tableLoad();
            clearFields();

        } catch (Exception ex) {
            try { conn.rollback(); } catch (Exception ignored) {}
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error Registering Faculty");
        }

    }//GEN-LAST:event_jButton2ActionPerformed
private void tableLoad() {

    try {
        pst = conn.prepareStatement(
            "SELECT u.user_id, u.full_name, u.department, u.username, " +
            "f.phone, f.fines, f.issued_books " +
            "FROM users u JOIN faculty f ON u.user_id = f.user_id"
        );

        rs = pst.executeQuery();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("user_id"),
                rs.getString("full_name"),
                rs.getString("department"),
                rs.getString("username"),
                rs.getString("phone"),
                rs.getBigDecimal("fines"),
                rs.getInt("issued_books")
            });
        }

    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error Loading Faculty Table");
    }
}
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
         int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a faculty first");
            return;
        }

        int userId = Integer.parseInt(jTable1.getValueAt(row, 0).toString());

        int confirm = JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to delete this faculty?",
            "Confirm Delete",
            JOptionPane.YES_NO_OPTION
        );

        if (confirm != JOptionPane.YES_OPTION) return;

        try {
            pst = conn.prepareStatement("DELETE FROM users WHERE user_id=?");
            pst.setInt(1, userId);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Faculty Deleted Successfully");
            tableLoad();
            clearFields();

        } catch (SQLException ex) {
            logger.log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error Deleting Faculty");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

